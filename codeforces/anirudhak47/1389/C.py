def ans(s,x):
    ana=0
    bo=0
    for i in range(len(x)):
        if(bo==0):
            if x[i]==s[0]:
               ana+=1
               bo=1
               continue
        if(bo==1):
            if x[i]==s[1]:
                ana+=1
                bo=0
    
    
    return ana
